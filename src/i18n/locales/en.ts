// Widen string literal types from `en`'s `as const` shape so other locales
// can supply translated strings of the same shape. Without this, `Translations`
// would only accept the exact English literals.
type Widen<T> = T extends string
  ? string
  : T extends ReadonlyArray<infer U>
    ? Widen<U>[]
    : T extends object
      ? { [K in keyof T]: Widen<T[K]> }
      : T;

export type Translations = Widen<typeof en>;
